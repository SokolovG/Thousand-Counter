class PlayerStats {
  final int currentBolts;
  final int currentBarrels;
  final int totalBolts;
  final int totalBarrels;
  final int totalMagicNumbers;
  final bool wasOnBarrel;

  PlayerStats({
    required this.currentBolts,
    required this.currentBarrels,
    required this.totalBolts,
    required this.totalBarrels,
    required this.totalMagicNumbers,
    required this.wasOnBarrel,
  });
}
